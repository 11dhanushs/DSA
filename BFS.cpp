class Graph{
    public:
        map<int, bool> visited;
        map<int, list<int>> adj;
        void addEdge(int u, int v);
        void BFS(int s);
};
void Graph::addEdge(int u,int v)
{
    adj[u].push_back(v);
}
void Graph::BFS(int s)
{
    queue<int> q;
    list<int>::iterator i;
    q.push(s);
    while(!q.empty())
    {
        s = q.front();
        visited[s] = true;
        cout << s << " ";
        q.pop();
        for (i = adj[s].begin(); i != adj[s].end();i++)
        {
            if(visited[*i]!=true){
                visited[*i] = true;
                q.push(*i);
            }
        }
    }
}
